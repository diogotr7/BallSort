namespace PascalPort;

public struct VialTopInfo
{
    /// <summary>
    /// How many slots are empty at the top of the vial
    /// </summary>
    public int Empty;
    
    /// <summary>
    /// The color of the top of the vial
    /// </summary>
    public Ball TopCol;
    
    /// <summary>
    /// How many balls of the top color are at the top of the vial
    /// </summary>
    public int TopVol;
}